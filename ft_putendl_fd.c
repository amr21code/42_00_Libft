#include "libft.h"

void	ft_putendl_fd(char *str, int fd)
{
	int	count;

	count = 0;
	while (str[count] != '\0')
	{
		write(fd, &str[count], 1);
		count++;
	}
	write(fd, "\n", 1);
}
